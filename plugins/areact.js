let AUTO_REACT_MESSAGES = false;
let lastReactedTime = 0;

function random(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

module.exports = {
  command: 'رياكت_تلقائي',
  aliases: ['رياكت'],
  category: 'owner',
  description: 'رياكت ع الرساله تلقائي',
  usage: '.رياكت_تلقائي تفعيل/تعطيل',
  ownerOnly: true,
  
  async handler(sock, message, args, context) {
    const { chatId, channelInfo } = context;
    
    if (!args[0] || !['تفعيل', 'تعطيل'].includes(args[0])) {
      await sock.sendMessage(chatId, {
        text: '*الاستخدام* 👀❤️\n.رياكت_تلقائي تفعيل/تعطيل 😊❤️',
        ...channelInfo
      }, { quoted: message });
      return;
    }

    AUTO_REACT_MESSAGES = args[0] === 'تفعيل';

    await sock.sendMessage(chatId, {
      text: AUTO_REACT_MESSAGES ? '*رياكت_تلقائي مفعل* 👀❤️' : '*رياكت_تلقائي معطل* 👀❤️',
      ...channelInfo
    }, { quoted: message });

    if (sock.__autoReactAttached) return;

    sock.ev.on('messages.upsert', async ({ messages }) => {
      if (!AUTO_REACT_MESSAGES) return;

      for (const m of messages) {
        if (!m?.message) continue;
        if (m.key.fromMe) continue;

        const text =
          m.message.conversation ||
          m.message.extendedTextMessage?.text ||
          '';

        if (!text) continue;
        if (/^[!#.$%^&*+=?<>]/.test(text)) continue;

        const now = Date.now();
        if (now - lastReactedTime < 1000) continue;

        await sock.sendMessage(m.key.remoteJid, {
          react: {
            text: random(autoEmojis),
            key: m.key
          }
        });

        lastReactedTime = now;
      }
    });

    sock.__autoReactAttached = true;
  }
};
